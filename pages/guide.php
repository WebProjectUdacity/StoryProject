  <p>Hello fellow writer. You might ask yourself "How does it work?". You have to worry no more.

  You start by registering yourself to the page. You can do that by clicking on the login button
  and  opening up the SignUp menue. Are you ready? Fine. After you did that just login on the same page and open the writing app.
  Here you see a tool bar on the  left,  the story in its current state in the middle, and submissions you can vote on on the right.
  At the bottom you can find the writing window. Just write your own part of the Story and submit it.

  Let your creativity run free! </p>

